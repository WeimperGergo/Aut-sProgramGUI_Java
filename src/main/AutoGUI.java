    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         i = jComboBox1.getSelectedIndex();
        
        if(autok[i].getUzemanyag() && autok[i].getBeinditva() && !autok[i].getDefekt()){
            autok[i].megy();
            jButton2ActionPerformed(evt);
        }
        else JOptionPane.showMessageDialog(rootPane, "Nem lehet menni az autóval. Ellenőrizd, hogy van-e üzemanyag, be van-e indítva és nincs-e defektje!");
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        i = jComboBox1.getSelectedIndex();
        
        if(!autok[i].getBeinditva()){
            if(!autok[i].getUzemanyag()){
                autok[i].tankol();
                jCheckBox1.setSelected(true);
                jButton2ActionPerformed(evt);
            }
            else JOptionPane.showMessageDialog(rootPane, "A kocsiban van üzemanyag!");
        }
        else JOptionPane.showMessageDialog(rootPane, "Nem lehet tankolni, le kell állítani az autót!");
    }//GEN-LAST:event_jButton4ActionPerformed

