    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        i = jComboBox1.getSelectedIndex();
        if(i > 0){
            String uzemanyag, beinditva, defekt, potkerek;
            uzemanyag = (autok[i].getUzemanyag()) ? "van" : "nincs";
            beinditva = (autok[i].getBeinditva()) ? "van" : "nincs";
            defekt = (autok[i].getDefekt()) ? "van" : "nincs";
            potkerek = (autok[i].getPotkerek() < 0) ? "nincs" : autok[i].getPotkerek() + " db";

            String autoMegjelenit =
            i + ". autó adatai:" +
            "\n  - Üzemanyag:\t" + uzemanyag +
            "\n  - Beindítva:\t" + beinditva +
            "\n  - Defekt:\t" + defekt +
            "\n  - Pótkerék:\t" + potkerek;
            jTextArea1.setText(autoMegjelenit);
        }
        else {
            jTextArea1.setText("");
            JOptionPane.showMessageDialog(rootPane, "Kérem, válasszon ki egy létező autót.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

